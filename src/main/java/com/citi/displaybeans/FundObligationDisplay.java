package com.citi.displaybeans;

public class FundObligationDisplay {
	private String clearingMemberName;
	private double fundObligationAmount;
	public String getClearingMemberName() {
		return clearingMemberName;
	}
	public void setClearingMemberName(String clearingMemberName) {
		this.clearingMemberName = clearingMemberName;
	}
	public double getFundObligationAmount() {
		return fundObligationAmount;
	}
	public void setFundObligationAmount(double fundObligationAmount) {
		this.fundObligationAmount = fundObligationAmount;
	}
	@Override
	public String toString() {
		return "FundObligationDisplay [clearingMemberName=" + clearingMemberName + ", fundObligationAmount="
				+ fundObligationAmount + "]";
	}
}
